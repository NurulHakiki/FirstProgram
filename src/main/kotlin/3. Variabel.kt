// variabel merupakan tempat yang digunakan untuk menyimpan data yang dapat diubah.
// dibawah ini terdapat 2 jenis type data system yakni

fun main(args: Array<String>) {
    var dynamicVar = 2
    println("Contoh dynamic variabel = " + dynamicVar)

    /*
    type sistem yang dinamis tidak memerlukan adanya deklarasi tipr data pada sebuah variabel,
    compiler secara otomatis mengenali tipe data dari sebuah variabel
     */

    var staticVar: Char = 'A'
    println("Contoh static variabel = " + staticVar)

    /*
    type data system static mengharuskan kita untuk mendeklarasikan jenis tipe data tertentu
     */
}